/* 
 * Un operador es un simbolo que le dice al compilador
 * que debe de realizar una tarea matematica, relacional o logica
 * y debe de producir un resultado
*/

void main() {
  int a = 10 + 5; // Suma

  a = 20 - 10; // Resta
  a = 10 * 2; // Multiplicacion

  double b = 10 / 2; // Division

  b = 10.0 % 3; // Modulo, da el residuo de una division

  int c = 10 ~/ 3; // Division comun y corriente

  int d = 1;

  d++; // Incrementa en uno el valor de la variable

  d--; // Decrementa en uno el valor de la variable

  d += 2; // Incrementa en dos el valor de la variable
}
